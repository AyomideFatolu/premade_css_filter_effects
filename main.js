
function copyBlur() {
 copy_blur("filter: blur(1px);");
 
  let a = document.getElementById("notification");
  a.className = "show";
  
  setTimeout(function(){ 
  a.className = a.className.replace("show", "");
   }, 3000);
}

function copy_blur (css_code_1){
  navigator.clipboard.writeText(css_code_1);
}


